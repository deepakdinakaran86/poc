package com.pcs.fault.monitor.bean;

import java.io.Serializable;

import com.pcs.fault.monitor.enums.ContactType;


/**
 * This class is responsible for holding contact details
 * 
 * @author pcseg129(Seena Jyothish) Jan 21, 2016
 */
public class Contact implements Serializable {

	private static final long serialVersionUID = 8394788850794110322L;

	String name;
	String email;
	String contactNumber;
	ContactType contactType;
	String rowIdentifier;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getContactNumber() {
		return contactNumber;
	}

	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}

	public ContactType getContactType() {
		return contactType;
	}

	public void setContactType(ContactType contactType) {
		this.contactType = contactType;
	}

	public String getRowIdentifier() {
		return rowIdentifier;
	}

	public void setRowIdentifier(String rowIdentifier) {
		this.rowIdentifier = rowIdentifier;
	}
	
}
