package com.pcs.fault.monitor.bean;

/**
 * This class is responsible for holding the configurations
 * 
 * @author pcseg129 (Seena Jyothish)
 * @date March 30 2015
 */
public class AnalyticsPlatform {
	
	String platformHost;
	String platformPort;
	String platformScheme;
	String platformUrl;
	String providerKey;
	Integer seed;
	
	public String getPlatformHost() {
		return platformHost;
	}
	public void setPlatformHost(String platformHost) {
		this.platformHost = platformHost;
	}
	public String getPlatformPort() {
		return platformPort;
	}
	public void setPlatformPort(String platformPort) {
		this.platformPort = platformPort;
	}
	public String getPlatformScheme() {
		return platformScheme;
	}
	public void setPlatformScheme(String platformScheme) {
		this.platformScheme = platformScheme;
	}
	public String getPlatformUrl() {
		return platformUrl;
	}
	public void setPlatformUrl(String platformUrl) {
		this.platformUrl = platformUrl;
	}
	public String getProviderKey() {
		return providerKey;
	}
	public void setProviderKey(String providerKey) {
		this.providerKey = providerKey;
	}
	public Integer getSeed() {
		return seed;
	}
	public void setSeed(Integer seed) {
		this.seed = seed;
	}
	
}
