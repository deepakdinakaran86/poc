package com.pcs.util.faultmonitor.ccd.fault.bean;

import com.fasterxml.jackson.annotation.JsonProperty;

public class FaultParameter {

	@JsonProperty("Name")
	private String name;
	
	@JsonProperty("Value")
	private String value;
	
	public FaultParameter(){
		
	}
	
	public FaultParameter(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
}
