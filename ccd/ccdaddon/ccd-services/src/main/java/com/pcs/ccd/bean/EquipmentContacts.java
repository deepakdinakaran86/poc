package com.pcs.ccd.bean;

import java.io.Serializable;
import java.util.List;

/**
 * This class is responsible for defining all fields required for attaching contacts to equipment
 * 
 * @author pcseg129(Seena Jyothish)
 * Feb 8, 2016
 */
public class EquipmentContacts implements Serializable{
	
    private static final long serialVersionUID = -2840456895310589993L;
	
    private String equipmentId;
	private List<String> contactIds;
	private String tenantId;
	
	public List<String> getContactIds() {
		return contactIds;
	}
	public void setContactIds(List<String> contactIds) {
		this.contactIds = contactIds;
	}
	public String getEquipmentId() {
		return equipmentId;
	}
	public void setEquipmentId(String equipmentId) {
		this.equipmentId = equipmentId;
	}
	public String getTenantId() {
		return tenantId;
	}
	public void setTenantId(String tenantId) {
		this.tenantId = tenantId;
	}
	

}
