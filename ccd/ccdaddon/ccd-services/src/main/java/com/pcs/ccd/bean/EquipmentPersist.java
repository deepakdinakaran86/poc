package com.pcs.ccd.bean;

import java.io.Serializable;
import java.util.List;

/**
 * This class is responsible for fields required for persisting an eqipment for CCD
 * 
 * @author pcseg129(Seena Jyothish)
 * Feb 7, 2016
 */
public class EquipmentPersist implements Serializable{
	
    private static final long serialVersionUID = 8977706558829133688L;
	
    private List<Equipment> equipments;
    private DomainObj mydomain;
    private Tenant tenant;
    private String tenantMarkerTemplate;
    private String defaultTenantTemplate;
    private String equipMarkerTemplate;
    private HeirarchyObj parent;
	
    public List<Equipment> getEquipments() {
		return equipments;
	}
	public void setEquipments(List<Equipment> equipments) {
		this.equipments = equipments;
	}
	public DomainObj getMydomain() {
		return mydomain;
	}
	public void setMydomain(DomainObj mydomain) {
		this.mydomain = mydomain;
	}
	public Tenant getTenant() {
		return tenant;
	}
	public void setTenant(Tenant tenant) {
		this.tenant = tenant;
	}
	public String getTenantMarkerTemplate() {
		return tenantMarkerTemplate;
	}
	public void setTenantMarkerTemplate(String tenantMarkerTemplate) {
		this.tenantMarkerTemplate = tenantMarkerTemplate;
	}
	public String getDefaultTenantTemplate() {
		return defaultTenantTemplate;
	}
	public void setDefaultTenantTemplate(String defaultTenantTemplate) {
		this.defaultTenantTemplate = defaultTenantTemplate;
	}
	public String getEquipMarkerTemplate() {
		return equipMarkerTemplate;
	}
	public void setEquipMarkerTemplate(String equipMarkerTemplate) {
		this.equipMarkerTemplate = equipMarkerTemplate;
	}
	public HeirarchyObj getParent() {
		return parent;
	}
	public void setParent(HeirarchyObj parent) {
		this.parent = parent;
	}
	
}
