package com.pcs.ccd.bean;

import java.io.Serializable;
import java.util.List;

/**
 * This class is responsible for properties for persisting an equipment for a tenant
 * 
 * @author pcseg129(Seena Jyothish) Feb 17, 2016
 */
public class TenantEquipment implements Serializable{

    private static final long serialVersionUID = 6941942408155085662L;
	
    private List<Equipment> equipments;
	private Tenant tenant;

	public List<Equipment> getEquipments() {
		return equipments;
	}

	public void setEquipments(List<Equipment> equipments) {
		this.equipments = equipments;
	}

	public Tenant getTenant() {
		return tenant;
	}

	public void setTenant(Tenant tenant) {
		this.tenant = tenant;
	}

}
