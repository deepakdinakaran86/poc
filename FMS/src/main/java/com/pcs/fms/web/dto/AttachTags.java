package com.pcs.fms.web.dto;

import java.io.Serializable;
import java.util.List;

public class AttachTags implements Serializable {

	private static final long serialVersionUID = -6764529651188105693L;

	private IdentityDTO entity;

	private List<Tag> tags;

	public IdentityDTO getEntity() {
		return entity;
	}

	public void setEntity(IdentityDTO entity) {
		this.entity = entity;
	}

	public List<Tag> getTags() {
		return tags;
	}

	public void setTags(List<Tag> tags) {
		this.tags = tags;
	}

}
