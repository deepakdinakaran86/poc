package com.pcs.fms.web.dto;

import java.io.Serializable;
import java.util.List;

public class AssetTypeTemplateDTO implements Serializable {

	private static final long serialVersionUID = 5663261526853136596L;
	private String domainName;
	private String assetType;
	private List<String> assetTypeValues;
	private String status;
	
	public String getDomainName() {
		return domainName;
	}
	public void setDomainName(String domainName) {
		this.domainName = domainName;
	}
	public String getAssetType() {
		return assetType;
	}
	public void setAssetType(String assetType) {
		this.assetType = assetType;
	}
	public List<String> getAssetTypeValues() {
		return assetTypeValues;
	}
	public void setAssetTypeValues(List<String> assetTypeValues) {
		this.assetTypeValues = assetTypeValues;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}
