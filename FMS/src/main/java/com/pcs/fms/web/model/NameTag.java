package com.pcs.fms.web.model;

import java.io.Serializable;

import com.fasterxml.jackson.databind.annotation.JsonSerialize;

/**
 * @author PCSEG191 Daniela
 * @date JUNE 2016
 * @since FMS1.0.0
 * 
 */
@JsonSerialize
public class NameTag implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7006870922680644081L;

	private String platformEntity;

	private String domain;

	private String firstName;

	private String entityTemplate;

	private String identifier;

	public String getPlatformEntity() {
		return platformEntity;
	}

	public void setPlatformEntity(String platformEntity) {
		this.platformEntity = platformEntity;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getEntityTemplate() {
		return entityTemplate;
	}

	public void setEntityTemplate(String entityTemplate) {
		this.entityTemplate = entityTemplate;
	}

	public String getIdentifier() {
		return identifier;
	}

	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}

}