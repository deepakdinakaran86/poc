package com.pcs.device.gateway.hobbies.message;

import com.pcs.device.gateway.hobbies.message.data.type.HobbiesDataType;

public class HobbiesConfigurationParameter {

	private String parameterName;
	private HobbiesDataType parameterDataType;
	private String unit;
	private String parameterId;
	private Object parameterValue;
	private String parameterAccessType;
	
	
	public String getParameterName() {
		return parameterName;
	}
	public void setParameterName(String parameterName) {
		this.parameterName = parameterName;
	}
	public HobbiesDataType getParameterDataType() {
		return parameterDataType;
	}
	public void setParameterDataType(HobbiesDataType parameterDataType) {
		this.parameterDataType = parameterDataType;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getParameterId() {
		return parameterId;
	}
	public void setParameterId(String parameterId) {
		this.parameterId = parameterId;
	}
	public Object getParameterValue() {
		return parameterValue;
	}
	public void setParameterValue(Object parameterValue) {
		this.parameterValue = parameterValue;
	}
	public String getParameterAccessType() {
		return parameterAccessType;
	}
	public void setParameterAccessType(String parameterAccessType) {
		this.parameterAccessType = parameterAccessType;
	}
	
	
	
}
