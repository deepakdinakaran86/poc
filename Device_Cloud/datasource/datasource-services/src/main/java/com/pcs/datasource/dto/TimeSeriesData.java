package com.pcs.datasource.dto;

import java.io.Serializable;
import java.util.List;
import java.util.Map;

/**
 * ConfigDTO
 * 
 * @author pcseg323 (Greeshma)
 * @date March 2015
 * @since galaxy-1.0.0
 */
public class TimeSeriesData implements Serializable {

	private static final long serialVersionUID = 1L;
	private String displayName;
	private String systemTag;
	private List<TimeSeries> values;
	private Map<String, String> aggregatedData;

	public String getDisplayName() {
		return displayName;
	}

	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}

	public String getSystemTag() {
		return systemTag;
	}

	public void setSystemTag(String systemTag) {
		this.systemTag = systemTag;
	}

	public List<TimeSeries> getValues() {
		return values;
	}

	public void setValues(List<TimeSeries> values) {
		this.values = values;
	}

	public Map<String, String> getAggregatedData() {
		return aggregatedData;
	}

	public void setAggregatedData(Map<String, String> aggregatedData) {
		this.aggregatedData = aggregatedData;
	}

}
