package com.pcs.datasource.dto;

import java.io.Serializable;

/**
 * CommandDTO
 *
 * @description DTO which Comprises informations for commands
 *
 * @author Greeshma (pcseg323)
 * @date March 2015
 * @since galaxy-1.0.0
 */
public class G2021CommandDTO implements Serializable {

	private static final long serialVersionUID = 1L;

	private String command;
	private String sourceId;
	private Integer unitId;
	private Integer sessionId;
	private Short requestId;
	private Short pointId;
	private String data;
	private String dataType;
	private Short leaseTime; // G2021
	private String commandCode; // commandCode
	private Short priority; // priority

	public String getCommand() {
		return command;
	}
	public void setCommand(String command) {
		this.command = command;
	}
	public String getSourceId() {
		return sourceId;
	}
	public void setSourceId(String sourceId) {
		this.sourceId = sourceId;
	}
	public Integer getUnitId() {
		return unitId;
	}
	public void setUnitId(Integer unitId) {
		this.unitId = unitId;
	}
	public Integer getSessionId() {
		return sessionId;
	}
	public void setSessionId(Integer sessionId) {
		this.sessionId = sessionId;
	}

	public Short getRequestId() {
		return requestId;
	}
	public void setRequestId(Short requestId) {
		this.requestId = requestId;
	}

	public Short getPointId() {
		return pointId;
	}
	public void setPointId(Short pointId) {
		this.pointId = pointId;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}

	public Short getLeaseTime() {
		return leaseTime;
	}
	public void setLeaseTime(Short leaseTime) {
		this.leaseTime = leaseTime;
	}
	public String getCommandCode() {
		return commandCode;
	}
	public void setCommandCode(String commandCode) {
		this.commandCode = commandCode;
	}

	public Short getPriority() {
		return priority;
	}
	public void setPriority(Short priority) {
		this.priority = priority;
	}



}
