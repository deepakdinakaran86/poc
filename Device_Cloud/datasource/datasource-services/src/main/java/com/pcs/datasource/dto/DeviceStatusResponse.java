package com.pcs.datasource.dto;

import java.util.List;

/**
 * This class is responsible for device status response
 * 
 * @author pcseg129(Seena Jyothish)
 * Apr 20, 2016
 */
public class DeviceStatusResponse {
	List<DeviceStatus> deviceStatus;
	List<GeneralResponse> responses;
	
	public List<DeviceStatus> getDeviceStatus() {
		return deviceStatus;
	}
	public void setDeviceStatus(List<DeviceStatus> deviceStatus) {
		this.deviceStatus = deviceStatus;
	}
	public List<GeneralResponse> getResponses() {
		return responses;
	}
	public void setResponses(List<GeneralResponse> responses) {
		this.responses = responses;
	}

}
