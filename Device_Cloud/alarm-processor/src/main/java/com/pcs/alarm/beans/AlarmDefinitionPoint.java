package com.pcs.alarm.beans;

import java.io.Serializable;

import com.pcs.alarm.enums.DataTypes;

/**
 * This class is responsible for alert definition points
 * 
 * @author pcseg129(Seena Jyothish) Jul 26, 2016
 */
public class AlarmDefinitionPoint implements Serializable {
	private static final long serialVersionUID = 8815468603064152221L;
	
	private String displayName;
	private DataTypes dataType;
	private String deviceId;

	public String getDisplayName() {
		return displayName;
	}

	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}
	
	public DataTypes getDataType() {
		return dataType;
	}

	public void setDataType(DataTypes dataType) {
		this.dataType = dataType;
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

}
