package com.pcs.device.gateway.G2021.message;

public class LocationMessage extends G2021Message {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6716618859896681630L;

	private Float latitude;
	private Float longitude;
	private String reason;
	private Short geofenceId;
	
	public Float getLatitude() {
		return latitude;
	}
	public void setLatitude(Float latitude) {
		this.latitude = latitude;
	}
	public Float getLongitude() {
		return longitude;
	}
	public void setLongitude(Float longitude) {
		this.longitude = longitude;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public Short getGeofenceId() {
		return geofenceId;
	}
	public void setGeofenceId(Short geofenceId) {
		this.geofenceId = geofenceId;
	}
	
	public static String REASON_UNKNOWN = "UNKNOWN";
	public static String MOVING = "MOVING";
	public static String SERVICE_FLAG_RESET = "SERVICE_FLAG_RESET";
	public static String GEOFENCE_ENTRANCE = "GEOFENCE_ENTRANCE";
	public static String GEOFENCE_EXIT = "GEOFENCE_EXIT";
	

}
