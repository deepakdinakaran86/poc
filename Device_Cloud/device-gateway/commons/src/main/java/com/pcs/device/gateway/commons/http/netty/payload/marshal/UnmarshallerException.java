package com.pcs.device.gateway.commons.http.netty.payload.marshal;

import java.util.Arrays;

/**
 * @author pcseg310
 *
 */
public class UnmarshallerException extends Exception {

	private static final long serialVersionUID = -964510578025474780L;

	public UnmarshallerException() {
		super();
	}

	public UnmarshallerException(String message) {
		super(message);
	}

	public UnmarshallerException(String message, Throwable cause) {
		super(message, cause);
	}

	@Override
	public String toString() {
		return "UnmarshallerException [getMessage()=" + getMessage()
				+ ", getStackTrace()=" + Arrays.toString(getStackTrace()) + "]";
	}

}
