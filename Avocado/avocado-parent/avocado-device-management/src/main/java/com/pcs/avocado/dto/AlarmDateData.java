package com.pcs.avocado.dto;

import java.io.Serializable;
import java.util.List;

/**
 * This class is responsible for bundling alarm data grouped by a date
 * 
 * @author PCSEG129(Seena Jyothish)
 * Jul 16, 2015
 */
public class AlarmDateData implements Serializable{
    private static final long serialVersionUID = 6885392057449341754L;
    
	private Long date;
	List<AlarmMessage> alarmMessages;
	
	public Long getDate() {
		return date;
	}
	public void setDate(Long date) {
		this.date = date;
	}
	public List<AlarmMessage> getAlarmMessages() {
		return alarmMessages;
	}
	public void setAlarmMessages(List<AlarmMessage> alarmMessages) {
		this.alarmMessages = alarmMessages;
	}
	
	
}
