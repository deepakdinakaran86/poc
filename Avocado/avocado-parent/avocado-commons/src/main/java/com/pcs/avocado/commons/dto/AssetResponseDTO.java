package com.pcs.avocado.commons.dto;

import java.io.Serializable;
import java.util.List;

/**
 * DTO for AssetResponse
 * 
 * @author Twinkle (PCSEG297)
 * @date May 2016
 * @since Avocado-1.0.0
 */
public class AssetResponseDTO implements Serializable {

	private static final long serialVersionUID = -775482194445563861L;

	private EntityDTO asset;
	private EntityDTO assetType;
	private List<EntityDTO> points;

	public EntityDTO getAsset() {
		return asset;
	}

	public void setAsset(EntityDTO asset) {
		this.asset = asset;
	}

	public EntityDTO getAssetType() {
		return assetType;
	}

	public void setAssetType(EntityDTO assetType) {
		this.assetType = assetType;
	}

	public List<EntityDTO> getPoints() {
	    return points;
    }

	public void setPoints(List<EntityDTO> points) {
	    this.points = points;
    }
}
