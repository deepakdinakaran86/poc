package com.pcs.avocado.token;

import java.io.Serializable;
import java.util.Set;

/**
 * 
 * DTO for roleNames and permissions of a user
 * 
 * @author pcseg199 (Javid Ahammed)
 * @date November 2015
 * @since alpine-1.0.0
 */
public class UserPermissionsDTO implements Serializable {

	private static final long serialVersionUID = 5857552336377812611L;
	private Set<String> roleNames;
	private Set<String> permissions;

	public Set<String> getRoleNames() {
		return roleNames;
	}

	public void setRoleNames(Set<String> roleNames) {
		this.roleNames = roleNames;
	}

	public Set<String> getPermissions() {
		return permissions;
	}

	public void setPermissions(Set<String> permissions) {
		this.permissions = permissions;
	}

}
