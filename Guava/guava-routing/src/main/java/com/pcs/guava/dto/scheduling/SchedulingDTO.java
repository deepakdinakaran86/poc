package com.pcs.guava.dto.scheduling;

import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;


@XmlRootElement
public class SchedulingDTO {
	
	private String domain;
	
	private String scheduleName;
	
	private String description;
	
	private String routeName;
	
	private String startTime;
	
	private String endTime;
	
	private Integer daySpan;
	
	private List<SchedulePoiDTO> destinationPoints;

	public String getScheduleName() {
		return scheduleName;
	}

	public void setScheduleName(String scheduleName) {
		this.scheduleName = scheduleName;
	}


	public String getRouteName() {
		return routeName;
	}

	public void setRouteName(String routeName) {
		this.routeName = routeName;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public Integer getDaySpan() {
		return daySpan;
	}

	public void setDaySpan(Integer daySpan) {
		this.daySpan = daySpan;
	}

	public List<SchedulePoiDTO> getDestinationPoints() {
		return destinationPoints;
	}

	public void setDestinationPoints(List<SchedulePoiDTO> destinationPoints) {
		this.destinationPoints = destinationPoints;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
	
	
	
	

}
