package com.pcs.guava.commons.validation;

/**
 * GalaxyErrorCode
 * 
 * @description Interface for DataFields in Galaxy,any DataFields Enum should
 *              implement this class
 * @author Javid Ahammed (PCSEG199)
 * @date 04 November 2014
 * @since galaxy-1.0.0
 */
public interface DataFields {

	/**
	 * Interface method to be implemented to get the Field Name
	 * 
	 * @return
	 */
	public String getFieldName();

	/**
	 * Interface method to be implemented to get the Variable Name in the DTO
	 * 
	 * @return
	 */
	public String getVariableName();

	/**
	 * Interface method to be implemented to get the Description of the
	 * DataField
	 * 
	 * @return
	 */
	public String getDescription();

}
