package com.pcs.alpine.services.enums;

/**
 * TableNames Enum
 * 
 * @description Enum defining the table names in the DB.
 * @author Anish Prabhakaran(PCSEG271)
 * @date 25th Aug 2014
 * @since galaxy-1.0.0
 */
public enum TableNames {

	SERVER_VALIDATION("server_validation"), UI_VALIDATION_MASTER(
	        "ui_validation_master"), UI_VALIDATION("ui_validation"),
	PLATFORM_ENTITY("platform_entity"), ENTITY("entity"),
	GLOBAL_ENTITY_TEMPLATE_NAME("global_entity_template_name"),
	ENTITY_TEMPLATE("entity_template"), PLATFORM_ENTITY_TEMPLATE(
	        "platform_entity_template"), NODE_ENTITY_TEMPLATE(
	        "node_entity_template"), ALARM_DATA("alarm_data"),
	ENTITY_DATA_STORE("entity_data_store"),
	ENTITY_LIVE_DATA("entity_live_data"), UNIT_ID_COUNTER("unit_id_counter"),
	WRITE_BACK_ACTIVITY_LOG("writeback_activity_log"), WRITE_BACK_DEVICE(
	        "writeback_device"), WRITE_BACK_PARAM("writeback_param");

	private String name;

	TableNames(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	@Override
	public String toString() {
		return this.name;
	}
}
